<h1>Introduction to Cardano</h1>

<div class="section">
	<h2>1. A Brief History</h2>
	<p>
		Cardano was founded by Charles Hoskinson, a co-founder of Ethereum, and launched in 2017.
		Developed by Input Output Global (IOG), it's the first blockchain built from peer-reviewed
		research and formal methods.
	</p>
	<ul>
		<li><strong>2015</strong> – Development begins</li>
		<li><strong>2017</strong> – Cardano mainnet launches</li>
		<li><strong>2018–2021</strong> – Major phases: Byron, Shelley, Goguen, Basho</li>
		<li><strong>2022–Present</strong> – Voltaire (governance)</li>
	</ul>
</div>

<div class="section">
	<h2>2. The eUTXO Model</h2>
	<p>
		Cardano uses the Extended Unspent Transaction Output (eUTXO) model, an upgrade to Bitcoin’s UTXO
		system. Instead of tracking balances like Ethereum, Cardano tracks unspent outputs, improving
		parallelism and predictability.
	</p>
</div>

<div class="section">
	<h2>3. Staking and Stake Pools</h2>
	<p>
		Cardano runs on a Proof-of-Stake protocol called Ouroboros. ADA holders can delegate to stake
		pools and earn rewards while helping to secure the network.
	</p>
	<ul>
		<li>Non-custodial staking – ADA stays in your wallet</li>
		<li>Earn rewards by delegating to stake pools</li>
	</ul>
</div>

<div class="section">
	<h2>4. Smart Contracts</h2>
	<p>Cardano supports smart contracts through several languages:</p>
	<ul>
		<li><strong>Plutus</strong> – Functional, Haskell-based</li>
		<li><strong>Aiken</strong> – Lightweight and developer-friendly</li>
		<li><strong>Marlowe</strong> – Domain-specific for financial contracts</li>
	</ul>
</div>

<div class="section">
	<h2>5. Governance</h2>
	<p>
		Cardano is moving toward decentralized governance in the Voltaire era. Through Project Catalyst
		and upcoming on-chain governance, ADA holders will propose and vote on changes to the network.
	</p>
</div>
