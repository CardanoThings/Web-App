<h1>Beginner's Guide to REST APIs with Node.js</h1>

<h2>What is a REST API?</h2>
<p>
	A <strong>REST API</strong> allows software systems to communicate over the web using HTTP. It helps
	applications fetch or send data like user profiles, weather info, or messages. REST stands for "Representational
	State Transfer" and is a set of rules for building APIs.
</p>

<h2>Key Concepts</h2>
<table>
	<thead>
		<tr><th>Term</th><th>Meaning</th></tr>
	</thead>
	<tbody>
		<tr><td><strong>Client</strong></td><td>Sends the request (e.g. app or browser)</td></tr>
		<tr><td><strong>Server</strong></td><td>Responds with data (API)</td></tr>
		<tr><td><strong>HTTP Method</strong></td><td>GET, POST, PUT, DELETE</td></tr>
		<tr><td><strong>Endpoint</strong></td><td>URL to access (e.g. /books)</td></tr>
		<tr><td><strong>JSON</strong></td><td>Format for data exchange</td></tr>
	</tbody>
</table>

<h2>HTTP Methods</h2>
<table>
	<thead>
		<tr><th>Method</th><th>What it does</th><th>Example</th></tr>
	</thead>
	<tbody>
		<tr><td>GET</td><td>Read data</td><td>/books</td></tr>
		<tr><td>POST</td><td>Create new data</td><td>/books</td></tr>
		<tr><td>PUT</td><td>Update data</td><td>/books/1</td></tr>
		<tr><td>DELETE</td><td>Remove data</td><td>/books/1</td></tr>
	</tbody>
</table>

